@section('recipeTitle', 'Chocolate Cake')
@section('foodImage', 'https://images.media-allrecipes.com/userphotos/720x405/708879.jpg')

@section('ingredients')
  <p>2 cups white sugar</p>
  <p>1 3/4 cups all-purpose flour</p>
  <p>3/4 cup unsweetened cocoa powder</p>
  <p>1 1/2 teaspoons baking powder</p>
  <p>1 1/2 teaspoons baking soda</p>
  <p>1 teaspoon salt</p>
  <p>2 eggs</p>
  <p>1 cup milk</p>
  <p>1/2 cup vegetable oil</p>
  <p>2 teaspoons vanilla extract</p>
  <p>1 cup boiling water</p>
@endsection

@section('instructions')
  <p>Preheat oven to 350 degrees F (175 degrees C). Grease and flour two nine inch round pans.</p>
  <p>In a large bowl, stir together the sugar, flour, cocoa, baking powder, baking soda and salt. Add the eggs, milk, oil and vanilla, mix for 2 minutes on medium speed of mixer. Stir in the boiling water last. Batter will be thin. Pour evenly into the prepared pans.</p>
  <p>Bake 30 to 35 minutes in the preheated oven, until the cake tests done with a toothpick. Cool in the pans for 10 minutes, then remove to a wire rack to cool completely.</p>
@endsection

