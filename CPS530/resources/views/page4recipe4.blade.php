@section('recipeTitle', 'Fried rice')
@section('foodImage', 'https://www.gimmesomeoven.com/wp-content/uploads/2017/07/How-To-Make-Fried-Rice-Recipe-2-1.jpg')

@section('ingredients')
  <p>Cooked,chilled, rice</p>
  <p>Eggs</p>
  <p>Carrots, onions, green onions and peas</p>
  <p>Garlic: Freshly minced</p>
  <p>Soy sauce</p>
  <p>Oyster sauce</p>
  <p>Toasted sesame oil</p>
  <p>Butter</p>
@endsection

@section('instructions')
  <p>Scramble your eggs: Using a small pat of butter, scramble some eggs in a large sauté pan, breaking them into small pieces as you go.  Then transfer the eggs to a separate plate, and set aside.</p>
  <p>Sauté your veggies and garlic: Return your sauté pan to the heat, and sauté the onions, carrots, peas and garlic until soft and cooked through.</p>
  <p>Stir fry rice: Then turn the burner to high heat.  Scooch the veggies over to one side of the pan, melt the remaining butter in the other half, and add the chilled rice, soy sauce, and oyster sauce (if using).  Then stir to combine with the veggies and continue sautéing the rice, stirring every 15-20 seconds or so for 3 minutes, or until you notice the rice and veggies starting to brown slightly.</p>
  <p>Remove pan from heat. And stir in your green onions, sesame oil, and scrambled eggs.</p>
  <p>Taste and season.  Give the rice a taste, and season with salt and pepper, plus any extra soy sauce or sesame oil if needed.</p>
  <p>Serve!  Then dish it up and serve nice and warm!</p>
@endsection

